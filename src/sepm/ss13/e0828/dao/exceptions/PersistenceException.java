package sepm.ss13.e0828.dao.exceptions;

/** This exception is used when there was some kind of persistence
 * problem like connection or statement creation errors
 * @author Maurizio Rinder u0828852
 *
 */
public class PersistenceException extends Exception {

	private static final String ERRORLABLE = "PersistanceError: ";
	
	public PersistenceException(String msg) {
		super(ERRORLABLE + msg);
	}
	
}
